import { Component } from '@angular/core';
import { map } from 'rxjs/operators';
import { Breakpoints, BreakpointObserver } from '@angular/cdk/layout';
import { Chart, registerables } from 'chart.js';
import { MatDialog } from '@angular/material/dialog';
import { CarbonFootprintComponent } from '../carbon-footprint/carbon-footprint.component';
import { CarbonFootprintOffsetComponent } from '../carbon-footprint-offset/carbon-footprint-offset.component';
Chart.register(...registerables);

@Component({
  selector: 'app-dash',
  templateUrl: './dash.component.html',
  styleUrls: ['./dash.component.css']
})
export class DashComponent {
  /** Based on the screen size, switch from standard to one column per row */
  cards = this.breakpointObserver.observe(Breakpoints.Handset).pipe(
    map(({ matches }) => {
      if (matches) {
        return [
          { title: 'Card 1', cols: 1, rows: 1 },
          { title: 'Card 2', cols: 1, rows: 1 },
          { title: 'Card 3', cols: 1, rows: 1 },
          { title: 'Card 4', cols: 1, rows: 1 }
        ];
      }

      return [
        { title: 'Card 1', cols: 2, rows: 1 },
        { title: 'Card 2', cols: 1, rows: 1 },
        { title: 'Card 3', cols: 1, rows: 2 },
        { title: 'Card 4', cols: 1, rows: 1 }
      ];
    })
  );

  constructor(private breakpointObserver: BreakpointObserver, private dialog: MatDialog) {}

  offset() {
    this.dialog.open(CarbonFootprintOffsetComponent);
  }

  emission() {
    this.dialog.open(CarbonFootprintComponent);
  }
}
